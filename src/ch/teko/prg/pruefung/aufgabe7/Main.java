package ch.teko.prg.pruefung.aufgabe7;

public class Main {

    public static void main(String[] args) {
        int a = 0;
        while (a < 10) {
            a++;
        }
        System.out.println(a);

        int b = 0;
        for (b = 0; b < 10; b++){
            b++;
            System.out.println(b);
        }
        System.out.println(b);
    }
}
